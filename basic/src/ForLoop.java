public class ForLoop {

    public static void main(String[] args) {

        // print counting using loop
        for (int i = 0; i < 100; i++)
            System.out.print(i + 1 + " ");


        // print name using loop    
        String name = "ram kumar";
        for (int i = 1; i <= 5; i++) {
            System.out.println(name);
        }

        // print table
        for(int i = 1; i<=10; i++){
            System.out.println( 5 + " * " + i + " = " + 5 * i);
        }
    }

}
