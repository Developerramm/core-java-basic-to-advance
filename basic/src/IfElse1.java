public class IfElse1 {

    public static void main(String[] args) {
        int number = 0;

        if (number > 0) {
            System.out.println("Number is positive here ");
        } else if (number < 0) {
            System.out.println("Number is negative here ");

        } else {
            System.out.println("Number is zero here ");
        }
    }

}
