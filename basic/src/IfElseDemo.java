public class IfElseDemo {

    public static void main(String[] args) {
        int age = 40;

        if (age >= 18) {
            System.out.println("You can vote now");
        } else {
            System.out.println("You cannot vote now ");
        }

        String language = "Java";

        if (language == "Java") {
            System.out.println("Best Programming Language");
        }
    }

}
