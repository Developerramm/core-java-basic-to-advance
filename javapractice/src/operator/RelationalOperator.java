package operator;

public class RelationalOperator {
    public static void main(String[] args) {
        int p = 20;
        int q = 10;

        System.out.println( p == q);
        System.out.println(p != q);
        System.out.println( p > q);

        System.out.println(p < q);

        System.out.println(q >= q);
        System.out.println( p <= q);

        System.out.println(p > q && q < p);


    }

}
